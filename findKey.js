// Implement the function findKey that takes object and callback
const findKey = function(objects, callback) {
  // Loop over the objects
  for (const key in objects) {
    // If the value is the callback's object's key's value, then return the key
    if (callback(objects[key])) {
      return key;
    }
  }
};


